//incrementDecrementOperators.cpp
#include<iostream>
using namespace std;

int main()
{
	int a =10;
	cout<<"\nThe Value of a = "<<a;
	cout<<"\nUsing a++ we get: \n";
	cout<<a++;
	cout<<"\nAfter a++ the value of a is "<<a;
	cout<<"\nUsing ++a we get:\n";
	cout<<++a;
	cout<<"\nNow the Value of a is "<<a;
	return 0;
}